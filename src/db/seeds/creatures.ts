import z from 'zod'

/**
 * Zod schema for an SRD creature entry.
 *
 * Extends the base creature shape with `size` and `category` fields that
 * capture the physical size category and monster type from the 5.1 SRD.
 * Initiative is always `'roll'` (DEX modifier) for SRD creatures.
 */
export const srdCreatureValidator = z.object({
  id: z.string(),
  name: z.string().min(1),
  initiativeType: z.enum(['fixed', 'roll']),
  initiative: z.number().int(),
  hp: z.number().int().nonnegative(),
  size: z.enum(['Tiny', 'Small', 'Medium', 'Large', 'Huge', 'Gargantuan']),
  category: z.string().min(1),
  categoryIds: z.array(z.string()),
})

